function renderBoardMembers() {
    const seniorGrid = document.getElementById('senior-grid-container');
    const juniorGrid = document.getElementById('junior-grid-container');
    const singleGrid = document.getElementById('board-grid-container');

    const seniorMembers = [];
    const juniorMembers = [];

    boardMembers.forEach(member => {
        const nameLower = member.name.toLowerCase().trim();
        const isSenior = seniorMemberNames.some(sName => nameLower.includes(sName) || sName.includes(nameLower));
        if (isSenior) {
            seniorMembers.push(member);
        } else {
            juniorMembers.push(member);
        }
    });

    if (seniorGrid) {
        seniorGrid.innerHTML = seniorMembers.map(createMemberCardHtml).join('');
    }
    if (juniorGrid) {
        juniorGrid.innerHTML = juniorMembers.map(createMemberCardHtml).join('');
    }
    if (singleGrid) {
        singleGrid.innerHTML = boardMembers.map(createMemberCardHtml).join('');
    }

    if (typeof gsap !== 'undefined') {
        gsap.to('.board-card', {
            opacity: 1,
            y: 0,
            duration: 0.5,
            stagger: 0.04,
            ease: "power2.out"
        });
    }
}

document.addEventListener('DOMContentLoaded', () => {
    renderBoardMembers();
});
